package com.example.wallpaperapp.DATA;

public class CategoryNxtList {

    String id;
    String images;

    public CategoryNxtList(String id, String images) {
        this.id = id;
        this.images = images;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getImages() {
        return images;
    }

    public void setImages(String images) {
        this.images = images;
    }
}
